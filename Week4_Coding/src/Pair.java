class Pair
{
    private int x;
    private int y;

    // ------------------
    Pair(int x, int y)
    {
        this.x = x;
        this.y = y;
    } // End constructor

    // ---------------------
    public int getX()
    {
        return x;
    } // getX

    // ---------------------
    public int getY()
    {
        return y;
    } // getY

} // End class
